									<h3>En esta segunda entrevista hemos podido contar con colaboración de una chica de 25 años diagnosticada de TDAH y TEA. En este caso nos hemos preocupado por entender como fue su proceso de diagnóstico y su vida actualmente en la educación universitaria.</h3>
													

									<h3>¿Cómo acabaste siendo diagnosticada, quién se dio cuenta?</h3>
										<div class="box">
											<p>Cuando llegué a la universidad me interesaba por contemplar la atención a la diversidad en los
trabajos y empecé a leer cosas sobre ello, como sobre Dislexia, y me sentía identificada, pero
yo sabía que lo que me pasaba era en parte por “no prestar atención”, entre comillas. Además,
había algo más.
Después usaba el DSM-V y vi que muchas cosas del TEA me describían, me sentía identificada,
casi como si de alguna forma me describiera. Aun así, no pensaba en ello y de alguna manera
no lo contemplaba como una opción, ya que había ido con anterioridad a psicólogos y nunca
vieron nada así. De todas formas, no me habían atendido demasiado bien. Dejé pasar el tema
hasta que mi salud mental empezó a empeorar cada vez más, y fui a un psicólogo de la
seguridad social una vez más. Cuando le empecé a hablar, él me comentó la posibilidad de
tener TEA, bastante convencido, pero cuando me derivó a otro de otro centro para hacer las
pruebas pertinentes este segundo dijo que no tengo TEA porque “miro a los ojos y sonrío”.
Hablé con un profesor y le comenté lo que me pasaba y me recomendó una entidad privada
para que me diagnosticasen. Allí me hicieron la valoración de TEA, pero también vieron una
falta de atención importante y consideraron oportuno mirar si también tenía TDAH, estaban
convencidos. Actualmente me han diagnosticado TDAH en la Seguridad Social.
</p>

										</div>
										<h3>¿Cómo es tu día a día yendo a la universidad?
</h3>
<div class="box">
<p>Más complicado de lo que algunas personas creen. Tengo que coger el bus, pero nunca sé a
qué hora ni a qué hora tengo que ir a clase. Voy a un ambiente que me estresa. A veces estoy
mal, no me he visto con fuerza de hacer trabajos en grupo y lo he tenido que hacer
igualmente. Alguna vez he recurrido a la modalidad no presencial. En general, diría que para
venir a clase he tenido que dejar mi vida de lado porque no he tenido fuerzas para más. Mi
forma de pensar es que los demás no tienen que hacerme un favor ni tratarme diferente, pero
considero que en la universidad deberían ser más flexibles con la asistencia y la forma de
evaluación. Todos tenemos dificultades y necesitamos cosas distintas en distintos momentos
de nuestra vida y estar tan controlados siendo personas adultas no me tiene ningún sentido.
Muchas veces tenía que llorar antes de salir de casa para poder ir a clase.
</p>
</div>
<h3>¿Cómo te gustaría que fuese la universidad?

</h3>
<div class="box">
<p>Entiendo que las cosas no son así, pero si pudiera elegir cómo sería para mí una clase ideal,
consistiría en que el profesor diera la clase, irme a mi casa a estudiar tranquila y hacer un
examen al final. Si hubiese trabajos me gustaría que tuvieran una finalidad con una utilidad
real. Con la mayoría de los trabajos que hago no aprendo nada.</p>
